package stringPrograms;
//An immutable class 
public final class ImmutableString {
	
	 
	 final  String name; 
	  final int regNo; 
	  
	     public ImmutableString(String name, int regNo) 
	    { 
	        this.name = name; 
	        this.regNo = regNo; 
	    } 
	    public String getName() 
	    { 
	        return name; 
	    } 
	    public int getRegNo() 
	    { 
	        return regNo; 
	    } 
	} 
	  

	

